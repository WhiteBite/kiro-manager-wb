const localesDir = path.join(__dirname, '../src/webview/i18n/locales');

for (const [lang, trans] of Object.entries(translations)) {
    const filePath = path.join(localesDir, `${lang}.ts`);

    if (!fs.existsSync(filePath)) {
        console.log(`Skipping ${lang} - file not found`);
        continue;
    }

    let content = fs.readFileSync(filePath, 'utf8');

    // Find and replace the scheduled reg block
    const startMarker = '// Scheduled Registration';
    const endMarker = '// Proxy Settings';

    const startIdx = content.indexOf(startMarker);
    const endIdx = content.indexOf(endMarker);

    if (startIdx === -1 || endIdx === -1) {
        console.log(`${lang}: Could not find markers`);
        continue;
    }

    // Build the translation block
    const transBlock = `// Scheduled Registration
  scheduledRegistration: '${trans.scheduledRegistration}',
  scheduledRegistrationDesc: '${trans.scheduledRegistrationDesc}',
  loginTemplate: '${trans.loginTemplate}',
  loginTemplatePlaceholder: '${trans.loginTemplatePlaceholder}',
  loginTemplateHint: '${trans.loginTemplateHint}',
  startNumber: '${trans.startNumber}',
  scheduleInterval: '${trans.scheduleInterval}',
  scheduleIntervalManual: '${trans.scheduleIntervalManual}',
  scheduleIntervalMinutes: '${trans.scheduleIntervalMinutes}',
  maxAccounts: '${trans.maxAccounts}',
  maxAccountsHint: '${trans.maxAccountsHint}',
  startSchedule: '${trans.startSchedule}',
  stopSchedule: '${trans.stopSchedule}',
  scheduleActive: '${trans.scheduleActive}',
  scheduleNextIn: '${trans.scheduleNextIn}',
  complete: '${trans.complete}',
  preview: '${trans.preview}',
  nextNumber: '${trans.nextNumber}',
  interval: '${trans.interval}',
  minutes: '${trans.minutes}',
  hour: '${trans.hour}',
  hours: '${trans.hours}',
  startScheduled: '${trans.startScheduled}',
  resetProgress: '${trans.resetProgress}',
  noTimer: '${trans.noTimer}',
  noTimerHint: '${trans.noTimerHint}',
  custom: '${trans.custom}',
  upcoming: '${trans.upcoming}',
  startFrom: '${trans.startFrom}',
  enableFirst: '${trans.enableFirst}',
  registerOne: '${trans.registerOne}',

  `;

    // Replace content between markers
    const newContent = content.substring(0, startIdx) + transBlock + content.substring(endIdx);

    fs.writeFileSync(filePath, newContent);
    console.log(`${lang}: Updated successfully`);
}

console.log('Done!');
